    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultMutableTreeNode new_child = new DefaultMutableTreeNode(jTextField1.getText());
        DefaultMutableTreeNode selection_node = (DefaultMutableTreeNode)jTree1.getLastSelectedPathComponent();
        DefaultTreeModel tree_model = (DefaultTreeModel)jTree1.getModel();
        if(selection_node != null){
            tree_model.insertNodeInto(new_child,selection_node, selection_node.getChildCount() );
        }
        parent.baslik_degistir(jTextField1.getText());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultMutableTreeNode selected_node = (DefaultMutableTreeNode)jTree1.getLastSelectedPathComponent();
        DefaultTreeModel tree_model = (DefaultTreeModel)jTree1.getModel();
        if(selected_node != null){
            tree_model.removeNodeFromParent(selected_node);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

