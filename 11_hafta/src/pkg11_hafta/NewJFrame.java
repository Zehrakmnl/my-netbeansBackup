    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Component[] c = this.getContentPane().getComponents();
        
        for(int i = 0; i < c.length; i++){
            if(c[i] instanceof JLabel){
                JLabel tmp = (JLabel) c[i];
                tmp.setForeground(Color.BLUE);
                tmp.setText("FLabel");
            }
            else if(c[i] instanceof JCheckBox){
                JCheckBox tmp2 = (JCheckBox) c[i];
                tmp2.setForeground(Color.GREEN);
                tmp2.setText("FCheckBox");
            }
            else if(c[i] instanceof JRadioButton){
                JRadioButton tmp3 = (JRadioButton) c[i];
                tmp3.setForeground(Color.YELLOW);
                tmp3.setText("FRadioButton");
            }
            else if(c[i] instanceof JButton){
                JButton tmp4 = (JButton) c[i];
                tmp4.setForeground(Color.PINK);
                tmp4.setText("FButton");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

